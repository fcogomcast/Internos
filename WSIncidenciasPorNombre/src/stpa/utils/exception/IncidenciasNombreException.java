package stpa.utils.exception;

public class IncidenciasNombreException extends Exception {

	private static final long serialVersionUID = 8589112648811614752L;

	public IncidenciasNombreException() {
		super();
	}

	public IncidenciasNombreException(String message, Throwable cause) {
		super(message, cause);
	}

	public IncidenciasNombreException(String message) {
		super(message);
	}

	public IncidenciasNombreException(Throwable cause) {
		super(cause);
	}
}
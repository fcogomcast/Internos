package stpa.utils.exception;

public class ControlHorarioException extends Exception {

	private static final long serialVersionUID = 8589112648811614752L;

	public ControlHorarioException() {
		super();
	}

	public ControlHorarioException(String message, Throwable cause) {
		super(message, cause);
	}

	public ControlHorarioException(String message) {
		super(message);
	}

	public ControlHorarioException(Throwable cause) {
		super(cause);
	}
}